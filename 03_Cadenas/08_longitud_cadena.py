# Obetern el largo de una cadena
"""
Para obtener la longitud de una cadena utilizamos la funcion incorporada len()
La funcion len funciona con varios tipos de datos uncluyendo cadena, listas, etc.

Cuando se calcula el largo de una cadena se toman en cuenta todos los caracteres de una cadena,
incluyendo espacion en blanco, caracteres espaciales, etc.

Ej.
    cadena1 = "Hola Mundo"
    longitud = len(cadena1)
"""

cadena = 'Hola, Mundo!'
longitud = len(cadena)
print(f'Cadena original: {cadena}')
print(f'Larog de la cadena: {longitud}')
