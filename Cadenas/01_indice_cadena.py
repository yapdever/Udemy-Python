# Detalles de una cadena
"""
Los caracteres de una cadena estan indexados de manera secuencial.

Por lo tanto, podemos acceder cada caracter indicando el indice del caracter que 
deseamos recuperar
"""
cadena1 = 'Hola Mundo'
print(cadena1)

# Recuperar el primer caracter
primer_caracter = cadena1[0]
print(primer_caracter)

# Recuperat el ultimo caracter
ultimo_caracter = cadena1[9]
print(ultimo_caracter)


