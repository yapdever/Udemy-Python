# Ejemplo de tipo de datos en Python

# Entero
edad = 10
print("Edad:", edad)

# Flotante
altura = 1.75
print("Altura:", altura)

# Cade de texto
nombre = "Hector"
print("Nombre:", nombre)

# Boolean
es_estuadiante = False
print("Es estudiante?:", es_estuadiante)

# None
variable_nula = None
# None es un tipo de dato especial que representa la ausencia de valor
print("Variable nula:", variable_nula)
